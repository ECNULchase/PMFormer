#!/usr/bin/env bash

CONFIG=$1
CHECKPOINT=$2
GPUS=$3
NUM_VOTE=$4
SAVE_PATH=$5
NNODES=${NNODES:-1}
NODE_RANK=${NODE_RANK:-0}
PORT=${PORT:-29500}
MASTER_ADDR=${MASTER_ADDR:-"127.0.0.1"}

PYTHONPATH="$(dirname $0)/..":$PYTHONPATH \
python -m torch.distributed.launch \
    --nnodes=$NNODES \
    --node_rank=$NODE_RANK \
    --master_addr=$MASTER_ADDR \
    --nproc_per_node=$GPUS \
    --master_port=$PORT \
    $(dirname "$0")/test.py \
    $CONFIG \
    $CHECKPOINT \
    --num_vote=$NUMVOTE \
    --save_path=$SAVE_PATH \
    --launcher pytorch \
    ${@:4}
